#!/usr/bin/node

/*
Defines a Rectangle class with a constructor that initializes only if
width and height properties are > 0. Prints Rectangle using X.
Rotates it by exchanging the value of width with height.
Doubles it by multiplying attributes by 2.
*/
class Rectangle {
  constructor (w, h) {
    if (w > 0 && h > 0) {
      this.width = w;
      this.height = h;
    }
  }

  print () {
    for (let i = 0; i < this.height; i++) {
      console.log('X'.repeat(this.width));
    }
  }

  rotate () {
    const temp = this.width;
    this.width = this.height;
    this.height = temp;
  }

  double () {
    this.width *= 2;
    this.height *= 2;
  }
}

module.exports = Rectangle;
